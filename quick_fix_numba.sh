#!/bin/bash
# Quick fix for Numba caching error
# realtime-whisper-subtitles-optimized
# Encoding: UTF-8

set -e

echo "? Numba????????? ?????????"
echo "================================================"
echo ""

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

log_info() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

log_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

log_warning() {
    echo -e "${YELLOW}[WARNING]${NC} $1"
}

log_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

# Check if running in correct directory
if [ ! -f "docker-compose.yml" ]; then
    log_error "docker-compose.yml ????????"
    log_error "?????????????????????????"
    exit 1
fi

# Stop containers
log_info "????????..."
docker-compose down || true

# Backup original files
log_info "??????????????..."
mkdir -p backup_numba_fix
cp Dockerfile backup_numba_fix/Dockerfile.backup 2>/dev/null || true
cp requirements.txt backup_numba_fix/requirements.txt.backup 2>/dev/null || true
cp docker-compose.yml backup_numba_fix/docker-compose.yml.backup 2>/dev/null || true
log_success "????????"

# Apply quick fix to docker-compose.yml
log_info "docker-compose.yml ????..."
if grep -q "NUMBA_DISABLE_JIT=0" docker-compose.yml; then
    # Replace existing Numba config
    sed -i 's/NUMBA_DISABLE_JIT=0/NUMBA_DISABLE_JIT=1/g' docker-compose.yml
    log_success "Numba?JIT?????????????"
else
    # Add Numba config after COMPUTE_TYPE
    sed -i '/COMPUTE_TYPE=auto/a\      - NUMBA_DISABLE_JIT=1\n      - NUMBA_CACHE_DIR=/tmp/numba_cache\n      - NUMBA_THREADING_LAYER=workqueue' docker-compose.yml
    log_success "Numba?????????"
fi

# Clean up Docker cache
log_info "Docker??????????????..."
docker system prune -f >/dev/null 2>&1 || true

# Rebuild and start
log_info "??????????..."
if docker-compose build --no-cache whisper-subtitles; then
    log_success "?????"
else
    log_error "??????????"
    exit 1
fi

log_info "????????..."
if docker-compose up -d; then
    log_success "????"
else
    log_error "?????????"
    exit 1
fi

# Wait and check
log_info "30????..."
sleep 30

log_info "??????????..."
if curl -f http://localhost:8000/health >/dev/null 2>&1; then
    log_success "? ????! ???????????????????"
    echo ""
    echo "? ????URL: http://localhost:8000"
    echo "? ????: docker-compose logs -f whisper-subtitles"
else
    log_warning "????????????????..."
    log_info "??????? http://localhost:8000 ???????????"
    echo ""
    echo "? ????????:"
    echo "docker-compose logs whisper-subtitles"
fi

echo ""
log_success "Numba????????????!"
